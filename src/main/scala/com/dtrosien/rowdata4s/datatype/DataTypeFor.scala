/** A [[DataTypeFor]] generates an Avro Schema for a Scala or Java type.
  *
  * For example, a DataTypeFor[String] could return a datatype of type DataType.STRING, and a DataTypeFor[Int] could
  * return a datatype of type DataType.INT
  */
trait DataTypeFor[T] {
  self =>

  /** Returns the flink [[DataType]] generated by this typeclass
    */
  def dataType: DataType

  /** Changes the type of this DataTypeFor to the desired type `U` without any other modifications.
    *
    * @tparam U
    *   new type for DataTypeFor.
    */
  final def forType[U]: DataTypeFor[U] = map[U](identity)

  /** Creates a DataTypeFor[U] by applying a function DataType => DataType to the datatype generated by this instance.
    */
  final def map[U](fn: DataType => DataType): DataTypeFor[U] = {
    new DataTypeFor[U] {
      override def dataType: DataType = fn(self.dataType)
    }
  }
}

object DataTypeFor
    extends PrimitiveSchemas
    with BigDecimalSchemas
    with MagnoliaDerivedDataTypes
    with ByteIterableSchemas
    with CollectionSchemas
    with OptionSchemas
    with StringSchemas
    with TemporalSchemas
    with TupleSchemas {

  /** Returns a [[SchemaFor]] that returns the given schema.
    */
  def apply[T](t: DataType): DataTypeFor[T] = new DataTypeFor[T] {
    override def dataType: DataType = t
  }

  def from[T](f: => DataType): DataTypeFor[T] = new DataTypeFor[T] {
    override def dataType: DataType = f
  }

  def apply[T](using dataTypeFor: DataTypeFor[T]): DataTypeFor[T] = dataTypeFor
}

