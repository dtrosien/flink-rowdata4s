trait PrimitiveSchemas:
  given intDataTypeFor: DataTypeFor[Int] = DataTypeFor[Int](DataTypes.INT.notNull)
  given DataTypeFor[Byte]                = intDataTypeFor.forType
  given DataTypeFor[Short]               = intDataTypeFor.forType
  given DataTypeFor[Long]                = DataTypeFor[Long](DataTypes.BIGINT.notNull)
  given DataTypeFor[Float]               = DataTypeFor[Float](DataTypes.FLOAT.notNull)
  given DataTypeFor[Double]              = DataTypeFor[Double](DataTypes.DOUBLE.notNull)
  given DataTypeFor[scala.Boolean]       = DataTypeFor[Boolean](DataTypes.BOOLEAN.notNull)
  given DataTypeFor[ByteBuffer]          = DataTypeFor[ByteBuffer](DataTypes.BYTES.notNull)

