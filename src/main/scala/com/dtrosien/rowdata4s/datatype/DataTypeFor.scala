trait ByteIterableSchemas:
  given ByteArraySchemaFor: DataTypeFor[Array[Byte]]   = DataTypeFor[Array[Byte]](DataTypes.BYTES.notNull)
  given ByteListSchemaFor: DataTypeFor[List[Byte]]     = ByteArraySchemaFor.forType
  given ByteSeqSchemaFor: DataTypeFor[Seq[Byte]]       = ByteArraySchemaFor.forType
  given ByteVectorSchemaFor: DataTypeFor[Vector[Byte]] = ByteArraySchemaFor.forType

// ==============================================
// Collections   ================================
// ==============================================

trait CollectionSchemas:

  private def buildIterableSchemaFor[C[X] <: Iterable[X], T](using dataTypeFor: DataTypeFor[T]): DataTypeFor[C[T]] =
    dataTypeFor.map(DataTypes.ARRAY(_).notNull)

  given [T](using dataTypeFor: DataTypeFor[T]): DataTypeFor[Array[T]] = new DataTypeFor[Array[T]]:
    override def dataType: DataType = DataTypes.ARRAY(dataTypeFor.dataType).notNull

  given [T](using dataTypeFor: DataTypeFor[T]): DataTypeFor[Seq[T]] = buildIterableSchemaFor[Seq, T]

  given [T](using DataTypeFor[T]): DataTypeFor[Set[T]] = buildIterableSchemaFor[Set, T]

  given [T](using DataTypeFor[T]): DataTypeFor[Vector[T]] = buildIterableSchemaFor[Vector, T]

  given [T](using dataTypeFor: DataTypeFor[T]): DataTypeFor[List[T]] = buildIterableSchemaFor[List, T]

  given [V](using dataTypeFor: DataTypeFor[V]): DataTypeFor[Map[String, V]] =
    dataTypeFor.map(DataTypes.MAP(DataTypes.STRING.notNull, _).notNull)

// ==============================================
// Option   =====================================
// ==============================================

trait OptionSchemas {

  given DataTypeFor[None.type] = NoneDataTypeFor

  given [T](using dataTypeFor: DataTypeFor[T]): DataTypeFor[Option[T]] = new DataTypeFor[Option[T]] {
    override def dataType: DataType = dataTypeFor.dataType.nullable()
  }
}

object NoneDataTypeFor extends DataTypeFor[None.type]:
  private val s                   = DataTypes.NULL()
  override def dataType: DataType = s

