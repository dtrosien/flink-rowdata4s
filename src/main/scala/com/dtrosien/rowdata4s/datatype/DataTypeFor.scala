case class ScalePrecision(scale: Int, precision: Int)

object ScalePrecision {
  given default: ScalePrecision = ScalePrecision(2, 8)
}

trait BigDecimalSchemas:
  given (using sp: ScalePrecision): DataTypeFor[BigDecimal] = BigDecimalSchemaFor(sp)

class BigDecimalSchemaFor(sp: ScalePrecision) extends DataTypeFor[BigDecimal]:
  override def dataType: DataType = DataTypes.DECIMAL(sp.precision, sp.scale).notNull

