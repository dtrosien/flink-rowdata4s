trait TemporalSchemas:
  given InstantSchemaFor: DataTypeFor[Instant] =
    DataTypeFor(DataTypes.TIMESTAMP(3).notNull)
  // java.util.Date behaves like an instant
  given UtilDateSchemaFor: DataTypeFor[java.util.Date] =
    DataTypeFor(DataTypes.TIMESTAMP_WITH_LOCAL_TIME_ZONE(3).notNull)
  given SqlDateSchemaFor: DataTypeFor[java.sql.Date] =
    DataTypeFor(DataTypes.DATE.notNull)
  given LocalDateSchemaFor: DataTypeFor[LocalDate] =
    DataTypeFor(DataTypes.DATE.notNull)
  given LocalDateTimeSchemaFor: DataTypeFor[LocalDateTime] =
    DataTypeFor(DataTypes.BIGINT.notNull)
  given OffsetDateTimeSchemaFor: DataTypeFor[OffsetDateTime] =
    DataTypeFor(DataTypes.STRING.notNull)
  given LocalTimeSchemaFor: DataTypeFor[LocalTime] =
    DataTypeFor(DataTypes.TIME(6).notNull)
  given TimestampSchemaFor: DataTypeFor[Timestamp] =
    DataTypeFor(DataTypes.TIMESTAMP(3).notNull)

// ==============================================
// Tuples   =====================================
// ==============================================

trait TupleSchemas:

  given [A, B](using a: DataTypeFor[A], b: DataTypeFor[B]): DataTypeFor[Tuple2[A, B]] = DataTypeFor.from {
    val fields = java.util.ArrayList[DataTypes.Field]()
    fields.add(DataTypes.FIELD("_1", a.dataType))
    fields.add(DataTypes.FIELD("_2", b.dataType))
    DataTypes.ROW(fields).notNull
  }

  given [A, B, C](using a: DataTypeFor[A], b: DataTypeFor[B], c: DataTypeFor[C]): DataTypeFor[Tuple3[A, B, C]] =
    DataTypeFor.from {
      val fields = java.util.ArrayList[DataTypes.Field]()
      fields.add(DataTypes.FIELD("_1", a.dataType))
      fields.add(DataTypes.FIELD("_2", b.dataType))
      fields.add(DataTypes.FIELD("_3", c.dataType))
      DataTypes.ROW(fields).notNull
    }

  given [A, B, C, D](using
      a: DataTypeFor[A],
      b: DataTypeFor[B],
      c: DataTypeFor[C],
      d: DataTypeFor[D]
  ): DataTypeFor[Tuple4[A, B, C, D]] = DataTypeFor.from {
    val fields = java.util.ArrayList[DataTypes.Field]()
    fields.add(DataTypes.FIELD("_1", a.dataType))
    fields.add(DataTypes.FIELD("_2", b.dataType))
    fields.add(DataTypes.FIELD("_3", c.dataType))
    fields.add(DataTypes.FIELD("_4", d.dataType))
    DataTypes.ROW(fields).notNull
  }

  given [A, B, C, D, E](using
      a: DataTypeFor[A],
      b: DataTypeFor[B],
      c: DataTypeFor[C],
      d: DataTypeFor[D],
      e: DataTypeFor[E]
  ): DataTypeFor[Tuple5[A, B, C, D, E]] = DataTypeFor.from {
    val fields = java.util.ArrayList[DataTypes.Field]()
    fields.add(DataTypes.FIELD("_1", a.dataType))
    fields.add(DataTypes.FIELD("_2", b.dataType))
    fields.add(DataTypes.FIELD("_3", c.dataType))
    fields.add(DataTypes.FIELD("_4", d.dataType))
    fields.add(DataTypes.FIELD("_5", e.dataType))
    DataTypes.ROW(fields).notNull
  }

  given [A, B, C, D, E, F](using
      a: DataTypeFor[A],
      b: DataTypeFor[B],
      c: DataTypeFor[C],
      d: DataTypeFor[D],
      e: DataTypeFor[E],
      f: DataTypeFor[F]
  ): DataTypeFor[Tuple6[A, B, C, D, E, F]] = DataTypeFor.from {
    val fields = java.util.ArrayList[DataTypes.Field]()
    fields.add(DataTypes.FIELD("_1", a.dataType))
    fields.add(DataTypes.FIELD("_2", b.dataType))
    fields.add(DataTypes.FIELD("_3", c.dataType))
    fields.add(DataTypes.FIELD("_4", d.dataType))
    fields.add(DataTypes.FIELD("_5", e.dataType))
    fields.add(DataTypes.FIELD("_6", e.dataType))
    DataTypes.ROW(fields).notNull
  }
